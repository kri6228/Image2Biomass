def calculate_regression_metrics(
    y_true: np.ndarray,
    y_pred: np.ndarray,
    target_names: list[str] = TARGET_COLUMNS,
) -> pd.DataFrame:
    """
    Calculate regression metrics independently for each target.

    Metrics:

    - Mean Absolute Error (MAE)
    - Root Mean Squared Error (RMSE)
    - R-squared (R2)

    Parameters
    ----------
    y_true : numpy.ndarray
        Ground-truth target matrix.

    y_pred : numpy.ndarray
        Predicted target matrix.

    target_names : list[str], default=TARGET_COLUMNS
        Ordered target names.

    Returns
    -------
    pandas.DataFrame
        Per-target regression metrics.
    """

    if y_true.shape != y_pred.shape:
        raise ValueError(
            f"Shape mismatch: y_true={y_true.shape}, "
            f"y_pred={y_pred.shape}."
        )

    if y_true.ndim != 2:
        raise ValueError(
            "y_true and y_pred must be 2D arrays."
        )

    if y_true.shape[1] != len(target_names):
        raise ValueError(
            "Number of target columns does not match target names."
        )

    metric_records = []

    for target_index, target_name in enumerate(target_names):

        target_true = y_true[:, target_index]
        target_pred = y_pred[:, target_index]

        mae = mean_absolute_error(
            target_true,
            target_pred,
        )

        rmse = np.sqrt(
            mean_squared_error(
                target_true,
                target_pred,
            )
        )

        r2 = r2_score(
            target_true,
            target_pred,
        )

        metric_records.append(
            {
                "target": target_name,
                "MAE": mae,
                "RMSE": rmse,
                "R2": r2,
            }
        )

    metrics = pd.DataFrame(metric_records)

    return metrics


