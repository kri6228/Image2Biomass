# CROSS-VALIDATION
# ============================================================


def run_fold_cross_validation(
    feature_table: pd.DataFrame,
    feature_columns: list[str],
    model_builder: Callable[[], RegressorMixin],
    model_name: str,
    feature_set_name: str,
    fold_column: str = "fold",
    target_columns: list[str] = TARGET_COLUMNS,
) -> tuple[np.ndarray, pd.DataFrame]:
    """
    Run fold-based cross-validation and generate OOF predictions.

    For every fold:

    1. Use all other folds for model training.
    2. Train the requested regression model.
    3. Predict the held-out validation fold.
    4. Store predictions in original row positions.
    5. Calculate validation metrics independently for every target.

    Parameters
    ----------
    feature_table : pandas.DataFrame
        Combined feature table.

    feature_columns : list[str]
        Predictive feature columns.

    model_builder : Callable
        Function returning a new unfitted regression model.

    model_name : str
        Human-readable model name.

    feature_set_name : str
        Human-readable feature configuration name.

    fold_column : str, default="fold"
        Cross-validation fold column.

    target_columns : list[str], default=TARGET_COLUMNS
        Regression target columns.

    Returns
    -------
    tuple
        oof_predictions : numpy.ndarray
            OOF prediction matrix aligned with original table rows.

        fold_metrics : pandas.DataFrame
            Per-fold and per-target regression metrics.
    """

    if fold_column not in feature_table.columns:
        raise ValueError(
            f"Fold column '{fold_column}' is missing."
        )

    missing_features = [
        column
        for column in feature_columns
        if column not in feature_table.columns
    ]

    if missing_features:
        raise ValueError(
            f"Missing feature columns: {missing_features[:10]}"
        )

    missing_targets = [
        column
        for column in target_columns
        if column not in feature_table.columns
    ]

    if missing_targets:
        raise ValueError(
            f"Missing target columns: {missing_targets}"
        )

    folds = sorted(
        feature_table[fold_column]
        .unique()
        .tolist()
    )

    y_all = feature_table[
        target_columns
    ].to_numpy(dtype=np.float32)

    oof_predictions = np.full(
        y_all.shape,
        np.nan,
        dtype=np.float32,
    )

    fold_metric_tables = []

    for fold in folds:

        train_mask = (
            feature_table[fold_column] != fold
        )

        validation_mask = (
            feature_table[fold_column] == fold
        )

        X_train = feature_table.loc[
            train_mask,
            feature_columns,
        ].to_numpy(dtype=np.float32)

        X_validation = feature_table.loc[
            validation_mask,
            feature_columns,
        ].to_numpy(dtype=np.float32)

        y_train = feature_table.loc[
            train_mask,
            target_columns,
        ].to_numpy(dtype=np.float32)

        y_validation = feature_table.loc[
            validation_mask,
            target_columns,
        ].to_numpy(dtype=np.float32)

        model = model_builder()

        model.fit(
            X_train,
            y_train,
        )

        validation_predictions = model.predict(
            X_validation
        )

        validation_predictions = np.asarray(
            validation_predictions,
            dtype=np.float32,
        )

        validation_indices = np.flatnonzero(
            validation_mask.to_numpy()
        )

        oof_predictions[
            validation_indices
        ] = validation_predictions

        fold_metrics = calculate_regression_metrics(
            y_true=y_validation,
            y_pred=validation_predictions,
            target_names=target_columns,
        )

        fold_metrics.insert(
            0,
            "fold",
            fold,
        )

        fold_metrics.insert(
            0,
            "feature_set",
            feature_set_name,
        )

        fold_metrics.insert(
            0,
            "model",
            model_name,
        )

        fold_metric_tables.append(
            fold_metrics
        )

    if not np.isfinite(oof_predictions).all():
        raise ValueError(
            "OOF predictions contain NaN or infinite values."
        )

    all_fold_metrics = pd.concat(
        fold_metric_tables,
        ignore_index=True,
    )

    return (
        oof_predictions,
        all_fold_metrics,
    )


# ============================================================
# METRIC AGGREGATION
# ============================================================


def summarize_cv_metrics(
    fold_metrics: pd.DataFrame,
) -> pd.DataFrame:
    """
    Aggregate per-fold regression metrics.

    Parameters
    ----------
    fold_metrics : pandas.DataFrame
        Per-fold and per-target metrics generated during
        cross-validation.

    Returns
    -------
    pandas.DataFrame
        Mean and standard deviation of MAE, RMSE, and R2 for each
        model, feature set, and target.
    """

    required_columns = [
        "model",
        "feature_set",
        "target",
        "MAE",
        "RMSE",
        "R2",
    ]

    missing_columns = [
        column
        for column in required_columns
        if column not in fold_metrics.columns
    ]

    if missing_columns:
        raise ValueError(
            f"Missing metric columns: {missing_columns}"
        )

    summary = (
        fold_metrics
        .groupby(
            [
                "model",
                "feature_set",
                "target",
            ],
            as_index=False,
        )
        .agg(
            MAE_mean=("MAE", "mean"),
            MAE_std=("MAE", "std"),
            RMSE_mean=("RMSE", "mean"),
            RMSE_std=("RMSE", "std"),
            R2_mean=("R2", "mean"),
            R2_std=("R2", "std"),
        )
    )

    return summary


