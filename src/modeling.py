"""
Baseline modeling utilities for the CSIRO Image2Biomass project.

This module provides reusable functionality for:

- Defining image, metadata, and combined feature configurations.
- Building baseline regression models.
- Running fold-based cross-validation.
- Generating out-of-fold predictions.
- Calculating regression metrics for multiple biomass targets.
- Aggregating cross-validation results.
- Validating model predictions and outputs.

The functions are designed to work with the fold assignments and
feature artifacts generated by 03_feature_extraction.ipynb.
"""

from typing import Callable

import numpy as np
import pandas as pd

from sklearn.base import RegressorMixin
from sklearn.metrics import (
    mean_absolute_error,
    mean_squared_error,
    r2_score,
)
from sklearn.multioutput import MultiOutputRegressor
from sklearn.linear_model import Ridge
from sklearn.ensemble import RandomForestRegressor

from src.config import (
    RANDOM_SEED,
    RIDGE_ALPHA,
    RF_N_ESTIMATORS,
    RF_MAX_DEPTH,
    RF_MIN_SAMPLES_LEAF,
    RF_N_JOBS,
)


# ============================================================
# TARGET CONFIGURATION
# ============================================================


TARGET_COLUMNS = [
    "Dry_Clover_g",
    "Dry_Dead_g",
    "Dry_Green_g",
    "Dry_Total_g",
    "GDM_g",
]


# ============================================================
# FEATURE COLUMN IDENTIFICATION
# ============================================================


def get_feature_columns(
    feature_table: pd.DataFrame,
) -> dict[str, list[str]]:
    """
    Identify CNN, metadata, and combined predictive feature columns.

    Parameters
    ----------
    feature_table : pandas.DataFrame
        Combined feature table generated by Notebook 03.

    Returns
    -------
    dict[str, list[str]]
        Dictionary containing CNN-only, metadata-only, and combined
        feature column lists.

    Raises
    ------
    ValueError
        If CNN or metadata feature columns cannot be identified.
    """

    cnn_columns = [
        column
        for column in feature_table.columns
        if column.startswith("cnn_")
    ]

    excluded_columns = {
        "image_path",
        "processed_image_path",
        "fold",
        *TARGET_COLUMNS,
    }

    metadata_columns = [
        column
        for column in feature_table.columns
        if column not in excluded_columns
        and not column.startswith("cnn_")
    ]

    if len(cnn_columns) == 0:
        raise ValueError(
            "No CNN feature columns were identified."
        )

    if len(metadata_columns) == 0:
        raise ValueError(
            "No metadata feature columns were identified."
        )

    combined_columns = (
        cnn_columns
        + metadata_columns
    )

    return {
        "cnn": cnn_columns,
        "metadata": metadata_columns,
        "combined": combined_columns,
    }


