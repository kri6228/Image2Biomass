def validate_advanced_oof_predictions(
    predictions: np.ndarray,
    expected_samples: int,
    expected_targets: int = len(TARGET_COLUMNS),
) -> dict:
    """
    Validate advanced out-of-fold predictions.
    """

    expected_shape = (
        expected_samples,
        expected_targets,
    )

    if predictions.shape != expected_shape:
        raise ValueError(
            f"Expected prediction shape {expected_shape}, "
            f"received {predictions.shape}."
        )

    nan_count = int(
        np.isnan(predictions).sum()
    )

    inf_count = int(
        np.isinf(predictions).sum()
    )

    negative_count = int(
        np.sum(predictions < 0)
    )

    if nan_count > 0:
        raise ValueError(
            f"Predictions contain {nan_count} NaN values."
        )

    if inf_count > 0:
        raise ValueError(
            f"Predictions contain {inf_count} infinite values."
        )

    return {
        "n_samples": predictions.shape[0],
        "n_targets": predictions.shape[1],
        "dtype": str(predictions.dtype),
        "nan_count": nan_count,
        "inf_count": inf_count,
        "negative_prediction_count": negative_count,
        "prediction_min": float(np.min(predictions)),
        "prediction_max": float(np.max(predictions)),
        "prediction_mean": float(np.mean(predictions)),
        "prediction_std": float(np.std(predictions)),
    }


