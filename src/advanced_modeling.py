def run_advanced_cross_validation(
    feature_table: pd.DataFrame,
    feature_columns: list[str],
    model_builder: Callable,
    model_name: str,
    feature_set_name: str,
    fold_column: str = "fold",
    target_columns: list[str] = TARGET_COLUMNS,
    use_multioutput_wrapper: bool = False,
    clip_negative_predictions: bool = True,
    save_models: bool = False,
    model_output_dir: str | Path | None = None,
) -> tuple[np.ndarray, pd.DataFrame, dict]:
    """
    Run advanced fold-based cross-validation.

    For each fold:

    1. Select training and validation samples.
    2. Build a fresh regression model.
    3. Optionally wrap it using MultiOutputRegressor.
    4. Fit the model.
    5. Generate validation predictions.
    6. Optionally clip physically impossible negative predictions.
    7. Store aligned OOF predictions.
    8. Calculate per-target metrics.
    9. Optionally save fitted fold models.

    Returns
    -------
    tuple
        oof_predictions
        fold_metrics
        fitted_models
    """

    from sklearn.multioutput import MultiOutputRegressor

    missing_features = [
        column
        for column in feature_columns
        if column not in feature_table.columns
    ]

    if missing_features:
        raise ValueError(
            f"Missing feature columns: {missing_features[:10]}"
        )

    folds = sorted(
        feature_table[fold_column]
        .unique()
        .tolist()
    )

    y_all = feature_table[
        target_columns
    ].to_numpy(dtype=np.float32)

    oof_predictions = np.full(
        y_all.shape,
        np.nan,
        dtype=np.float32,
    )

    fold_metric_tables = []

    fitted_models = {}

    if save_models:

        if model_output_dir is None:
            raise ValueError(
                "model_output_dir must be provided "
                "when save_models=True."
            )

        model_output_dir = Path(model_output_dir)

        model_output_dir.mkdir(
            parents=True,
            exist_ok=True,
        )

    for fold in folds:

        train_mask = (
            feature_table[fold_column] != fold
        )

        validation_mask = (
            feature_table[fold_column] == fold
        )

        X_train = feature_table.loc[
            train_mask,
            feature_columns,
        ].to_numpy(dtype=np.float32)

        X_validation = feature_table.loc[
            validation_mask,
            feature_columns,
        ].to_numpy(dtype=np.float32)

        y_train = feature_table.loc[
            train_mask,
            target_columns,
        ].to_numpy(dtype=np.float32)

        y_validation = feature_table.loc[
            validation_mask,
            target_columns,
        ].to_numpy(dtype=np.float32)

        model = model_builder()

        if use_multioutput_wrapper:
            model = MultiOutputRegressor(model)

        model.fit(
            X_train,
            y_train,
        )

        validation_predictions = model.predict(
            X_validation
        )

        validation_predictions = np.asarray(
            validation_predictions,
            dtype=np.float32,
        )

        if clip_negative_predictions:
            validation_predictions = np.clip(
                validation_predictions,
                a_min=0.0,
                a_max=None,
            )

        validation_indices = np.flatnonzero(
            validation_mask.to_numpy()
        )

        oof_predictions[
            validation_indices
        ] = validation_predictions

        fold_metrics = calculate_advanced_regression_metrics(
            y_true=y_validation,
            y_pred=validation_predictions,
            target_names=target_columns,
        )

        fold_metrics.insert(
            0,
            "fold",
            fold,
        )

        fold_metrics.insert(
            0,
            "feature_set",
            feature_set_name,
        )

        fold_metrics.insert(
            0,
            "model",
            model_name,
        )

        fold_metric_tables.append(
            fold_metrics
        )

        fitted_models[fold] = model

        if save_models:

            model_path = (
                model_output_dir
                / f"{model_name}_{feature_set_name}_fold_{fold}.joblib"
            )

            joblib.dump(
                model,
                model_path,
            )

    if not np.isfinite(oof_predictions).all():
        raise ValueError(
            "OOF predictions contain NaN or infinite values."
        )

    all_fold_metrics = pd.concat(
        fold_metric_tables,
        ignore_index=True,
    )

    return (
        oof_predictions,
        all_fold_metrics,
        fitted_models,
    )


