def summarize_advanced_cv_results(
    fold_metrics: pd.DataFrame,
) -> pd.DataFrame:
    """
    Aggregate advanced CV metrics across folds.
    """

    summary = (
        fold_metrics
        .groupby(
            [
                "model",
                "feature_set",
                "target",
            ],
            as_index=False,
        )
        .agg(
            MAE_mean=("MAE", "mean"),
            MAE_std=("MAE", "std"),
            RMSE_mean=("RMSE", "mean"),
            RMSE_std=("RMSE", "std"),
            R2_mean=("R2", "mean"),
            R2_std=("R2", "std"),
        )
    )

    return summary


