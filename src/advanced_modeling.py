def validate_advanced_feature_table(
    feature_table: pd.DataFrame,
    fold_column: str = "fold",
) -> dict:
    """
    Validate the advanced modeling feature table.

    Checks:

    - Input is a DataFrame.
    - Table is non-empty.
    - Fold column exists.
    - All target columns exist.
    - CNN features exist.
    - Metadata features exist.
    - Visual features exist.
    - Predictive features contain no NaN values.
    - Predictive features contain no infinite values.
    - Target values contain no NaN or infinite values.

    Parameters
    ----------
    feature_table : pandas.DataFrame
        Complete modeling feature table.

    fold_column : str, default="fold"
        Cross-validation fold column.

    Returns
    -------
    dict
        Validation statistics.
    """

    if not isinstance(feature_table, pd.DataFrame):
        raise TypeError(
            "feature_table must be a pandas DataFrame."
        )

    if len(feature_table) == 0:
        raise ValueError(
            "feature_table must contain at least one row."
        )

    if fold_column not in feature_table.columns:
        raise ValueError(
            f"Fold column '{fold_column}' is missing."
        )

    missing_targets = [
        target
        for target in TARGET_COLUMNS
        if target not in feature_table.columns
    ]

    if missing_targets:
        raise ValueError(
            f"Missing target columns: {missing_targets}"
        )

    feature_groups = identify_advanced_feature_columns(
        feature_table
    )

    predictive_columns = feature_groups["all_features"]

    predictive_values = feature_table[
        predictive_columns
    ].to_numpy(dtype=np.float32)

    target_values = feature_table[
        TARGET_COLUMNS
    ].to_numpy(dtype=np.float32)

    predictive_nan_count = int(
        np.isnan(predictive_values).sum()
    )

    predictive_inf_count = int(
        np.isinf(predictive_values).sum()
    )

    target_nan_count = int(
        np.isnan(target_values).sum()
    )

    target_inf_count = int(
        np.isinf(target_values).sum()
    )

    if predictive_nan_count > 0:
        raise ValueError(
            f"Predictive features contain "
            f"{predictive_nan_count} NaN values."
        )

    if predictive_inf_count > 0:
        raise ValueError(
            f"Predictive features contain "
            f"{predictive_inf_count} infinite values."
        )

    if target_nan_count > 0:
        raise ValueError(
            f"Targets contain {target_nan_count} NaN values."
        )

    if target_inf_count > 0:
        raise ValueError(
            f"Targets contain {target_inf_count} infinite values."
        )

    return {
        "n_samples": len(feature_table),
        "n_folds": feature_table[fold_column].nunique(),
        "n_cnn_features": len(feature_groups["cnn"]),
        "n_metadata_features": len(feature_groups["metadata"]),
        "n_visual_features": len(feature_groups["visual"]),
        "n_total_features": len(feature_groups["all_features"]),
        "n_targets": len(TARGET_COLUMNS),
        "predictive_nan_count": predictive_nan_count,
        "predictive_inf_count": predictive_inf_count,
        "target_nan_count": target_nan_count,
        "target_inf_count": target_inf_count,
    }


