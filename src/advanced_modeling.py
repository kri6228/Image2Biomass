def save_oof_predictions(
    feature_table: pd.DataFrame,
    predictions: np.ndarray,
    output_path: str | Path,
    model_name: str,
    feature_set_name: str,
) -> Path:
    """
    Save aligned OOF predictions to CSV.
    """

    output_path = Path(output_path)

    output_path.parent.mkdir(
        parents=True,
        exist_ok=True,
    )

    prediction_table = pd.DataFrame(
        predictions,
        columns=[
            f"pred_{target}"
            for target in TARGET_COLUMNS
        ],
    )

    if "image_path" in feature_table.columns:
        prediction_table.insert(
            0,
            "image_path",
            feature_table["image_path"].values,
        )

    if "fold" in feature_table.columns:
        prediction_table.insert(
            1,
            "fold",
            feature_table["fold"].values,
        )

    prediction_table["model"] = model_name
    prediction_table["feature_set"] = feature_set_name

    prediction_table.to_csv(
        output_path,
        index=False,
    )

    return output_path