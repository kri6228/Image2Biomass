def rank_model_configurations(
    cv_summary: pd.DataFrame,
) -> pd.DataFrame:
    """
    Rank model and feature-set configurations using average
    performance across all biomass targets.

    Lower RMSE and MAE are better.
    Higher R2 is better.
    """

    ranking = (
        cv_summary
        .groupby(
            [
                "model",
                "feature_set",
            ],
            as_index=False,
        )
        .agg(
            mean_MAE=("MAE_mean", "mean"),
            mean_RMSE=("RMSE_mean", "mean"),
            mean_R2=("R2_mean", "mean"),
        )
        .sort_values(
            by=[
                "mean_RMSE",
                "mean_MAE",
            ],
            ascending=[
                True,
                True,
            ],
        )
        .reset_index(drop=True)
    )

    ranking.insert(
        0,
        "rank",
        np.arange(
            1,
            len(ranking) + 1,
        ),
    )

    return ranking


