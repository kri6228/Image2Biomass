def build_extra_trees_model(
    n_estimators: int = 500,
    max_depth: int | None = None,
    min_samples_leaf: int = 1,
    max_features: str | float | int | None = 1.0,
    n_jobs: int = -1,
) -> ExtraTreesRegressor:
    """
    Build an Extra Trees multi-output regression model.
    """

    return ExtraTreesRegressor(
        n_estimators=n_estimators,
        max_depth=max_depth,
        min_samples_leaf=min_samples_leaf,
        max_features=max_features,
        random_state=RANDOM_SEED,
        n_jobs=n_jobs,
    )


def build_hist_gradient_boosting_model(
    learning_rate: float = 0.05,
    max_iter: int = 300,
    max_leaf_nodes: int = 31,
    l2_regularization: float = 1.0,
) -> HistGradientBoostingRegressor:
    """
    Build a HistGradientBoosting regression model.

    Note
    ----
    HistGradientBoostingRegressor does not natively support
    multi-output regression. It should therefore be wrapped using
    sklearn.multioutput.MultiOutputRegressor before training.
    """

    return HistGradientBoostingRegressor(
        learning_rate=learning_rate,
        max_iter=max_iter,
        max_leaf_nodes=max_leaf_nodes,
        l2_regularization=l2_regularization,
        random_state=RANDOM_SEED,
    )


def build_lightgbm_model(
    n_estimators: int = 500,
    learning_rate: float = 0.03,
    num_leaves: int = 31,
    max_depth: int = -1,
    subsample: float = 0.8,
    colsample_bytree: float = 0.8,
    n_jobs: int = -1,
):
    """
    Build a LightGBM regression model.

    LightGBM is imported lazily so the rest of the module remains
    usable even when LightGBM is not installed.
    """

    try:
        from lightgbm import LGBMRegressor

    except ImportError as exc:
        raise ImportError(
            "LightGBM is not installed. Install it using "
            "'pip install lightgbm'."
        ) from exc

    return LGBMRegressor(
        n_estimators=n_estimators,
        learning_rate=learning_rate,
        num_leaves=num_leaves,
        max_depth=max_depth,
        subsample=subsample,
        colsample_bytree=colsample_bytree,
        random_state=RANDOM_SEED,
        n_jobs=n_jobs,
        verbosity=-1,
    )


