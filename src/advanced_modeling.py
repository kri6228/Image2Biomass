IDENTIFIER_COLUMNS = {
    "image_path",
    "processed_image_path",
    "fold",
}


def identify_advanced_feature_columns(
    feature_table: pd.DataFrame,
) -> dict[str, list[str]]:
    """
    Identify CNN, metadata, visual, and fused feature configurations.

    Expected feature naming conventions:

    - CNN features begin with ``cnn_``.
    - Visual features begin with one of the known visual prefixes.
    - Remaining predictive columns are treated as metadata features.

    Parameters
    ----------
    feature_table : pandas.DataFrame
        Complete modeling feature table.

    Returns
    -------
    dict[str, list[str]]
        Mapping containing:

        - cnn
        - metadata
        - visual
        - cnn_metadata
        - cnn_visual
        - metadata_visual
        - all_features

    Raises
    ------
    ValueError
        If required feature groups cannot be identified.
    """

    visual_prefixes = (
        "rgb_",
        "hsv_",
        "exg_",
        "vegetation_",
        "brown_",
        "hist_",
        "texture_",
        "edge_",
    )

    cnn_columns = [
        column
        for column in feature_table.columns
        if column.startswith("cnn_")
    ]

    visual_columns = [
        column
        for column in feature_table.columns
        if column.startswith(visual_prefixes)
    ]

    excluded_columns = (
        IDENTIFIER_COLUMNS
        | set(TARGET_COLUMNS)
        | set(cnn_columns)
        | set(visual_columns)
    )

    metadata_columns = [
        column
        for column in feature_table.columns
        if column not in excluded_columns
    ]

    if len(cnn_columns) == 0:
        raise ValueError(
            "No CNN feature columns were identified."
        )

    if len(metadata_columns) == 0:
        raise ValueError(
            "No metadata feature columns were identified."
        )

    if len(visual_columns) == 0:
        raise ValueError(
            "No handcrafted visual feature columns were identified."
        )

    return {
        "cnn": cnn_columns,
        "metadata": metadata_columns,
        "visual": visual_columns,
        "cnn_metadata": cnn_columns + metadata_columns,
        "cnn_visual": cnn_columns + visual_columns,
        "metadata_visual": metadata_columns + visual_columns,
        "all_features": (
            cnn_columns
            + metadata_columns
            + visual_columns
        ),
    }


