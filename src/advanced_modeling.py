def calculate_advanced_regression_metrics(
    y_true: np.ndarray,
    y_pred: np.ndarray,
    target_names: list[str] = TARGET_COLUMNS,
) -> pd.DataFrame:
    """
    Calculate MAE, RMSE, and R2 independently for every target.
    """

    if y_true.shape != y_pred.shape:
        raise ValueError(
            f"Shape mismatch: "
            f"y_true={y_true.shape}, "
            f"y_pred={y_pred.shape}."
        )

    records = []

    for target_index, target_name in enumerate(target_names):

        target_true = y_true[:, target_index]
        target_pred = y_pred[:, target_index]

        records.append(
            {
                "target": target_name,
                "MAE": mean_absolute_error(
                    target_true,
                    target_pred,
                ),
                "RMSE": np.sqrt(
                    mean_squared_error(
                        target_true,
                        target_pred,
                    )
                ),
                "R2": r2_score(
                    target_true,
                    target_pred,
                ),
            }
        )

    return pd.DataFrame(records)


